//Principios Importantes:
//1 - Abstração - o primcipio da abstração consiste na capacidade de se criar objetos computacionais apartir de objetos reais - por exemplo um carro é um objeto real, como transformar um carro em um objeto computacional? Isso consiste em criar um objeto computacional que tenha os atributos de um carro, como por exemplo: cor, portência, tipo de direção, tipo de cambio, etc., bem como seus métodos ou funções que o carro pode realizar como por exemplo: acelerar, freiar, buzinhar, refrigerar, etc.
//2 - Escapsulamento - o principio do encapsulamento consiste no principio que permite que os objetos contenham em si todas as suas funcionalidades e atributos sem depender ou dependendo minimamente de outros objetos, isto garante o baixo acoplamento entre os objetos e consequentemente entre os sistemas.
//3 - Herança (prototype) - herança é o principio de OO que permite de objetos de mesma natureza ou de naturezas semelhantes herdem de outros carateristicas especificas, por exemplo o objeto Veiculo tem como funcionalidade o metodo acelerar, ao criar dois novos objetos como carro e motocicleta estes poderiam simplesmente herdar o metdo acelerar ao inves de recriá-lo, este principio garante aproveitamento de código, evitando a criação da roda uma, duas, inumeras vezes.
//4 - Polimorfismo - este principio permite que se trabalhe com inumeros objetos trabalhando apenas um tipo de objeto, por exemplo um carro por assumir várias formas diferentes: Ferrari, Uno, Fusca, etc.



